from linalg import gaussian_elimination

A1 = [
    [1, 2],
    [2, 3],
]
b1 = [4, 5]

A2 = [
    [1, 2, 3],
    [2, 4, 6],
    [3, 5, 7],
]
b2 = [6,12,15]

A3 = [
    [1, 2, 3, 4],
    [2, 4, 6, 8],
    [3, 5, 7, 9],
]
b3 = [6,12,15]

A4 = [
    [1, 2, 1],
    [2, 4, 3],
]
b4 = [6, 7]

A5 = [
    [1,2,1,0,3],
    [2,4,2,1,7],
    [3,6,3,1,10],
]
b5 = [6,7,8]

A6 = [
    [1,1,0,2,0,3],
    [2,2,1,4,1,7],
    [3,3,1,6,1,10],
    [1,1,0,2,0,3],
]
b6 = [8,5,3,7]

A7 = [
    [1, 7, -10, 0, 3, 2, 7],
    [-8, 6, -5, -7, 4, -1, 5],
    [0, -5, 6, -7, 6, -3, 3],
    [4, -10, 9, -6, -9, -5, -5],
    [-5, -8, 6, -9, 2, 0, -6],
]
b7 = [100,70,50,300,90]

def test(A, b):
    A, b, is_free = gaussian_elimination(A, b)
    for i, row in enumerate(A):
        print('\t'.join([f"{n:.2f}" for n in row]), '\t\t', b[i])
    print('\t'.join([str(b) for b in is_free]))
    print()

for A, b in [
    (A1, b1),
    (A2, b2),
    (A3, b3),
    (A4, b4),
    (A5, b5),
    (A6, b6),
    (A7, b7),
]:
    test(A, b)
